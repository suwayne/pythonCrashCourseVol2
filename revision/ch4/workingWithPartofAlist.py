#slicing a list

players = ['charles', 'martina', 'michael', 'florence', 'eli']
print(players[0:3]) #in the print statement we decide the where to start and stop printing the list.


print(players[:4]) #this will print from the start of the list till the end.
print(players[2:]) #this will print from the 3rd item till the end of the list.

